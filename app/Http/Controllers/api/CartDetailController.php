     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $cartDetails = CartDetail::findOrFail($request->id);
        $cartDetails->quantity = $request->quantity;

        $cartDetails->save();
        return $cartDetails;
    }

